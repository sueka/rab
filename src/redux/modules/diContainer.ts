const diContainerActionTypes: unknown[] = []

export type DiContainerAction = never

function isDiContainerAction(action: Action): action is DiContainerAction {
  return diContainerActionTypes.includes(action.type)
}

//
//                           _|
// _|  _|_|    _|_|      _|_|_|  _|    _|    _|_|_|    _|_|    _|  _|_|
// _|_|      _|_|_|_|  _|    _|  _|    _|  _|        _|_|_|_|  _|_|
// _|        _|        _|    _|  _|    _|  _|        _|        _|
// _|          _|_|_|    _|_|_|    _|_|_|    _|_|_|    _|_|_|  _|
//
//

export const createDiContainerReducer: (initialState: DiContainerState) => Reducer<DiContainerState, Action> = (initialState) => (state = initialState, action) => {
  if (!isDiContainerAction(action)) {
    return state
  }

  throw new LogicError()
}
